package operators;

public class Arithmetic {
    public static void main(String[] args) {
        // Declare variables
        int a = 12;
        int b = 5;

        // Additon Oprator
        System.out.println("a + b = " + (a+b));
        // Substraction
        System.out.println("a - b = " + (a-b));
        // multiplication
        System.out.println("a * b = " + (a * b));
        //division
        System.out.println("a / b = " + (a/b));
        //modulo or remainder
        System.out.println("a % b = " + (a%b));
    }
}
