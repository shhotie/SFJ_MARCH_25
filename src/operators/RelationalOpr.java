package operators;

public class RelationalOpr {
    public static void main(String[] args) {
        // carete varibales
        int a = 7, b = 11;
        // value of a and b
        System.out.println("a is " + a + "and b is " + b);
         // ==
        System.out.println(a == b); // false
        System.out.println(a != b); // true
        System.out.println(a > b); //false
        System.out.println(a < b); // true
        System.out.println(a >= b); //f
        System.out.println(a <= b); //true


    }
}
