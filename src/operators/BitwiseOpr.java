package operators;

public class BitwiseOpr {
    public static void main(String[] args) {
        int x =5, y= 3;
        System.out.println(x ^ y);
        int a = 12;
        System.out.println(~a);
        int num1 = 5;
        int num2 = -5;
        System.out.println(num1 << 2);
        System.out.println(num2 << 2);
    }
}
